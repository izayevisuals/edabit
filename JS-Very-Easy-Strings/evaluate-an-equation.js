// Create a function that evaluates an equation.

// My Solution: 

// PREP
// Parameters - 'evaluate'
// Return - return eval(evaluate)
// Examples - eq("1+2") ➞ 3; eq("6/(9-7)") ➞ 3
// Pseudocode - return an evaluated string problem

function eq(evaluate) {
	return eval(evaluate)
}

eq('1 + 2')
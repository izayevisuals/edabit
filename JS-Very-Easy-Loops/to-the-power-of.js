// Task: To the Power of _____
// Create a function that takes a base number and an exponent number and returns the calculation.

// My Solution:

// PREP
// Parameters - 'num', 'exp'
// Return - the calculation of the base number and exponent number
// Examples - calculateExponent(5, 5) ➞ 3125; calculateExponent(10, 10) ➞ 10000000000;
// Pseudocode - 


// Code
function calculateExponent(num, exp) {
	return Math.pow(num, exp)
}

